package com.yjs.entity;

/*
* 区县表
* */
public class District {
    private int id ; //区县编号
    private String name;//区县名称

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
